// Совместимость чисел жизненного пути 1-9 (мастера сводим к 2/4/6)
const COMPAT_MATRIX: number[][] = [
  // row/col 1..9, 0-indexed
  [7, 6, 8, 4, 9, 5, 6, 7, 8], // 1
  [6, 8, 7, 9, 5, 9, 6, 7, 6], // 2
  [8, 7, 7, 5, 9, 8, 6, 6, 9], // 3
  [4, 9, 5, 8, 4, 9, 7, 9, 4], // 4
  [9, 5, 9, 4, 6, 6, 8, 8, 7], // 5
  [5, 9, 8, 9, 6, 9, 6, 7, 9], // 6
  [6, 6, 6, 7, 8, 6, 7, 4, 8], // 7
  [7, 7, 6, 9, 8, 7, 4, 7, 6], // 8
  [8, 6, 9, 4, 7, 9, 8, 6, 9], // 9
];

function toCompatBucket(n: number): number {
  if (n === 11) return 2;
  if (n === 22) return 4;
  if (n === 33) return 6;
  return Math.min(Math.max(n, 1), 9);
}

export function numerologyCompatibility(a: number, b: number): { score: number; text: string } {
  const x = toCompatBucket(a);
  const y = toCompatBucket(b);
  const raw = COMPAT_MATRIX[x - 1][y - 1];
  const text =
    raw >= 9 ? `Числа ${a} и ${b} — резонанс на уровне жизненной задачи. Поддерживаете друг друга в развитии без слов.`
    : raw >= 7 ? `Числа ${a} и ${b} — комплементарны. Разные сильные стороны складываются.`
    : raw >= 5 ? `Числа ${a} и ${b} — пара через осознанность. Ловите различия — это ваш материал.`
    : `Числа ${a} и ${b} — много трения. Нужны границы и зрелость с обеих сторон.`;
  return { score: raw * 10, text };
}
