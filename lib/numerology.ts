// Нумерология: число жизненного пути + совместимость.

function sumDigits(n: number): number {
  let s = 0;
  let x = Math.abs(n);
  while (x > 0) {
    s += x % 10;
    x = Math.floor(x / 10);
  }
  return s;
}

// Сохраняем мастер-числа 11, 22, 33 если выпали по пути
export function reduceWithMasters(n: number): number {
  let x = n;
  while (x > 9 && x !== 11 && x !== 22 && x !== 33) {
    x = sumDigits(x);
  }
  return x;
}

// Просто свернуть к 1-9 (без мастер-чисел)
export function reduceToDigit(n: number): number {
  let x = Math.abs(n);
  while (x > 9) x = sumDigits(x);
  return x;
}

// month: 1..12, day: 1..31, year: full (e.g. 1990)
export function lifePathNumber(year: number, month: number, day: number): number {
  return reduceWithMasters(sumDigits(year) + sumDigits(month) + sumDigits(day));
}

export function personalYear(year: number, month: number, day: number, currentYear: number): number {
  return reduceToDigit(sumDigits(currentYear) + sumDigits(month) + sumDigits(day));
}

