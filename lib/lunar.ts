// Лунный календарь: текущий лунный день, фаза, освещённость.
// Простой алгоритм через известное новолуние + синодический период 29.53059 дней.

const KNOWN_NEW_MOON = Date.UTC(2000, 0, 6, 18, 14, 0); // 6 января 2000, 18:14 UTC
const SYNODIC_MONTH = 29.53058867;
const DAY_MS = 86400000;

export type LunarPhase =
  | "new"
  | "waxing-crescent"
  | "first-quarter"
  | "waxing-gibbous"
  | "full"
  | "waning-gibbous"
  | "last-quarter"
  | "waning-crescent";

export const PHASE_RU: Record<LunarPhase, string> = {
  "new": "новолуние",
  "waxing-crescent": "растущий серп",
  "first-quarter": "первая четверть",
  "waxing-gibbous": "растущая луна",
  "full": "полнолуние",
  "waning-gibbous": "убывающая луна",
  "last-quarter": "последняя четверть",
  "waning-crescent": "стареющий серп",
};

export const PHASE_GLYPH: Record<LunarPhase, string> = {
  "new": "🌑",
  "waxing-crescent": "🌒",
  "first-quarter": "🌓",
  "waxing-gibbous": "🌔",
  "full": "🌕",
  "waning-gibbous": "🌖",
  "last-quarter": "🌗",
  "waning-crescent": "🌘",
};

export function lunarToday(now: Date = new Date()): {
  day: number;
  phase: LunarPhase;
  phaseRu: string;
  glyph: string;
  illumination: number; // 0..100
  age: number; // дней от новолуния
} {
  const diff = (now.getTime() - KNOWN_NEW_MOON) / DAY_MS;
  const age = ((diff % SYNODIC_MONTH) + SYNODIC_MONTH) % SYNODIC_MONTH;
  const fraction = age / SYNODIC_MONTH;
  const day = Math.floor(fraction * 30) + 1; // 1..30

  let phase: LunarPhase;
  if (fraction < 0.0625) phase = "new";
  else if (fraction < 0.1875) phase = "waxing-crescent";
  else if (fraction < 0.3125) phase = "first-quarter";
  else if (fraction < 0.4375) phase = "waxing-gibbous";
  else if (fraction < 0.5625) phase = "full";
  else if (fraction < 0.6875) phase = "waning-gibbous";
  else if (fraction < 0.8125) phase = "last-quarter";
  else if (fraction < 0.9375) phase = "waning-crescent";
  else phase = "new";

  const illumination = Math.round((1 - Math.cos(2 * Math.PI * fraction)) / 2 * 100);

  return {
    day: Math.min(day, 30),
    phase,
    phaseRu: PHASE_RU[phase],
    glyph: PHASE_GLYPH[phase],
    illumination,
    age,
  };
}

